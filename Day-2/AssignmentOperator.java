public class AssignmentOperator {
    public static void main(String[] args) {
        int a = 10;
        //a = a + 10;
        a += 10;
        System.out.println(a);

        int B = 15;
        //B = B - 10;
        B -= 10;
        System.out.println(B);

        int C = 18;
        //C = C * 10;
        C *= 10;
        System.out.println(C);

        int D = 20;
        //D = D / 10;
        D /= 10;
        System.out.println(D);

        int E = 18;
        //E = E % 10;
        E %= 10;
        System.out.println(E);

    }
    
}
