public class LogicalOperator {
    public static void main(String[] args) {
        //Logical AND Operator
        System.out.println((3>2) && (5>0));

        System.out.println((3<2) && (5>0));

        //Logical OR Operator
        System.out.println((3>2) || (5>0));

        System.out.println((3<2) || (5>0));

        //Logical NOT Operator
        System.out.println( !(3>2));

        System.out.println( !(3<2));
    }
    
}
